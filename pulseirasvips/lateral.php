<h3 class="direita">Pulseirismo </h3>
              <p>A tendência começou no fim do ano passado e ganhou força total no inverno. É o pulseirismo, mistura de várias pulseiras de diferentes cores e estilos usadas ao mesmo tempo. Segundo a estilista e consultora de imagem Cristina Rocha, o pulseirismo não tem muita regra nem limites.</p>
              <p>“A ideia é misturar cores, texturas, materiais, espessuras e estilos. Você pode misturar dourado com prata, com rosê, sem preocupação. A mistura de pulseiras com relógio também cria uma ótima composição”, diz. </p>
              <p>Ela faz uma única ressalva: buscar harmonia entre as peças e respeitar seu estilo pessoal. “Isso não significa que se você tem um estilo romântico não possa usar spikes, por exemplo. Pode sim, justamente para dar uma quebrada no romantismo. Mas você deve se sentir bem antes de tudo.”, explica.</p>
